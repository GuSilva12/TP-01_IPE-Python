''' 8) Entrar  via  teclado  com  o  valor  de  uma  temperatura  em  graus  Celsius,  
calcular  e  exibir  sua temperatura equivalente em Fahrenheit.
'''

c = input ("Digite a temperatura em graus celsius: ")
f = 1.8*int(c)+32

print ("A temperatura digitada em graus Celsius é: ", f, " Fahrenheit.")
